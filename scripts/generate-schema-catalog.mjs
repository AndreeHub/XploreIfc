import { execFileSync } from "node:child_process";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(root, "..");
const ifcRoot = path.resolve(projectRoot, "..", "IFCOpen_shell");
const pythonPath = path.resolve(ifcRoot, "Geometry", ".venv", "Scripts", "python.exe");
const schemaDocsDir = path.resolve(ifcRoot, "ifcopenshell", "util", "schema");

const docsBySchema = {
  IFC2X3: "ifc2x3_entities.json",
  IFC4: "ifc4_entities.json",
  IFC4X3: "ifc4x3_entities.json"
};

const pyCode = String.raw`
import json
import ifcopenshell

schemas = ["IFC2X3", "IFC4", "IFC4X3"]
result = {}

for schema_name in schemas:
    schema = ifcopenshell.schema_by_name(schema_name)
    schema_entities = {}
    for entity in schema.entities():
        chain = []
        current = entity
        while current:
            chain.append(current.name())
            current = current.supertype()
        chain.reverse()

        schema_entities[entity.name()] = {
            "abstract": entity.is_abstract(),
            "chain": chain,
            "attributes": [
                {
                    "name": attribute.name(),
                    "optional": attribute.optional(),
                    "type": str(attribute.type_of_attribute()),
                }
                for attribute in entity.all_attributes()
            ],
        }
    result[schema_name] = schema_entities

print(json.dumps(result, ensure_ascii=False))
`;

function readJson(fileName) {
  return JSON.parse(readFileSync(path.resolve(schemaDocsDir, fileName), "utf8"));
}

function cleanDescription(value) {
  if (!value || typeof value !== "string") return undefined;
  return value
    .replace(/<[^>]+>/g, " ")
    .replace(/\{[^}]+\}/g, " ")
    .replace(/&nbsp;/g, " ")
    .replace(/&quot;/g, '"')
    .replace(/&amp;/g, "&")
    .replace(/\s+/g, " ")
    .trim();
}

function attributeDescription(docs, chain, attributeName) {
  for (let i = chain.length - 1; i >= 0; i -= 1) {
    const entityDocs = docs[chain[i]];
    const attrDocs = entityDocs?.attributes?.[attributeName];
    const cleaned = cleanDescription(attrDocs);
    if (cleaned) return cleaned;
  }
  return undefined;
}

const schemaShape = JSON.parse(execFileSync(pythonPath, ["-c", pyCode], { encoding: "utf8", maxBuffer: 64 * 1024 * 1024 }));
const generated = {};
const classNames = new Set();

for (const [schemaName, docsFile] of Object.entries(docsBySchema)) {
  const docs = readJson(docsFile);
  const entities = {};

  for (const [entityName, entityShape] of Object.entries(schemaShape[schemaName])) {
    classNames.add(entityName);
    const entityDocs = docs[entityName] ?? {};
    entities[entityName] = {
      description: cleanDescription(entityDocs.description),
      specUrl: entityDocs.spec_url,
      abstract: Boolean(entityShape.abstract),
      attributes: entityShape.attributes.map((attribute) => ({
        name: attribute.name,
        optional: Boolean(attribute.optional),
        type: attribute.type,
        description: attributeDescription(docs, entityShape.chain, attribute.name)
      })),
      predefinedTypes: entityDocs.predefined_types
    };
  }

  generated[schemaName] = { entities };
}

const targetDir = path.resolve(projectRoot, "src", "data");
mkdirSync(targetDir, { recursive: true });
const output = `/* eslint-disable */\n// Generated by scripts/generate-schema-catalog.mjs.\nimport type { GeneratedSchemaCatalog } from \"../types/schema\";\n\nexport const generatedSchemaCatalog = ${JSON.stringify(generated, null, 2)} satisfies GeneratedSchemaCatalog;\n`;
writeFileSync(path.resolve(targetDir, "schemaCatalog.generated.ts"), output, "utf8");
const classOutput = `/* eslint-disable */\n// Generated by scripts/generate-schema-catalog.mjs.\nexport const generatedClassNames = ${JSON.stringify([...classNames].sort((a, b) => a.localeCompare(b)), null, 2)} as const;\n`;
writeFileSync(path.resolve(targetDir, "classNames.generated.ts"), classOutput, "utf8");
